
package org.alfresco.integrations.google.docs.exceptions;


public class GoogleDocsRefreshTokenException
    extends Exception
{

    /**
     * 
     */
    private static final long serialVersionUID = 1L;


    public GoogleDocsRefreshTokenException()
    {
        super();
    }


    public GoogleDocsRefreshTokenException(String message)
    {
        super(message);
    }


    public GoogleDocsRefreshTokenException(Throwable cause)
    {
        super(cause);
    }


    public GoogleDocsRefreshTokenException(String message, Throwable cause)
    {
        super(message, cause);
    }
}
